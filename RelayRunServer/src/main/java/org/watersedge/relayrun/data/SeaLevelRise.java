package org.watersedge.relayrun.data;

public class SeaLevelRise {
	private double longitude;
	private double latitude;
	private float seaLevelRise;
	private float waterDepth;
	public double getLongitude() {
		return longitude;
	}
	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}
	public double getLatitude() {
		return latitude;
	}
	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}
	public float getSeaLevelRise() {
		return seaLevelRise;
	}
	public void setSeaLevelRise(float seaLevelRise) {
		this.seaLevelRise = seaLevelRise;
	}
	public float getWaterDepth() {
		return waterDepth;
	}
	public void setWaterDepth(float waterDepth) {
		this.waterDepth = waterDepth;
	}
	
}
